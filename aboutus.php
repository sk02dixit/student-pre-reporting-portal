                    <p style="color:#006db5;">G.M.R.D. College, Mohanpur, Via- Baghra, District- Samastipur was established in June, 1960 By MAHANTH RAMESHWAR DAS and got affiliated by the BIHAR UNIVERSITY, Muzaffarpur in year 1968 up to degree standard. Then the college become a Constituent Unit under L.N.M. University, Darbhanga in year 1980. It was also registered under the UGC Act 2F in year 1982 and was Accredited Grad C by NAAC in year 2005.</p>
                    <p style="color:#006db5;">More than four decades back a saint named Shri Shri 108 Rameshwar Das of Garhi Math dreamed to establish an institution like this college. Though the saint was not literate but had high thinking about higher education for rural masses mostly backward classes and down traders of the region . The student under poverty line could matriculate any how and were deprived of higher education. That’s why this college was founded rural surrounding nearby Ganga River in the lap of nature.</p>
                    <p style="color:#006db5;">It remained in up/down procces for a long lastly came in to existence in 1960 affiliated by the University Of Bihar , Muzaffarpur and turned to constituent Unit of L.N.Mithila University, K. Nagar, Darbhanga in 1981. At that time Mahant Rameshwar Das constituted a committee of Late Ramdeo Roy the MLA, Late Rameshwar Kunwar, Late Alakhdeo Roy nad Late Shivchandra Prasad roy to help in this regard.</p>
                    <p style="color:#006db5;">The Mahant scarified himself economically and all that was possible. Thus the name of the college was given GARHI MAHANTH RAMESHWAR DAS COLLEGE, MOHANPUR. It is a co-education higher educational institute of this rural area in this district and oldest in North Bihar. It has its Large Campus and Infrastructure. It fulfills the academic need of higher co-education in the district. Apart from regular courses in Science, Social Science and Humanities, there is provision of Remedial Coaching and Entry Services Coaching sponsored by UGC for SC/ST/Minority. During the current year total number of student in this college in degree classes is about 1500 and in Intermediate classes is about 500. Total Sanctioned post of Teaching staff is 18 and Non-Teaching staff is 49, Out of which working Teacher is 11 and Non-Teaching is 22 only. Now we have developed our infrastructure of the new campus of the college for the benefit of girl students with the help of UGC fund. We got constructed the boundary wall of the college, Building for girls hostel, we have science and arts block separately, Library building etc.</p>
